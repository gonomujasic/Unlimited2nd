package min.point.dto;

public class RefundRequestDTO {
	private int no;
	private int dealing_point;
	private String done;
	private String id;
	private String request_date;
	private String refund_accountant;
	private String refund_bank;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getDealing_point() {
		return dealing_point;
	}
	public void setDealing_point(int dealing_point) {
		this.dealing_point = dealing_point;
	}
	public String getDone() {
		return done;
	}
	public void setDone(String done) {
		this.done = done;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRequest_date() {
		return request_date;
	}
	public void setRequest_date(String request_date) {
		this.request_date = request_date;
	}
	public String getRefund_accountant() {
		return refund_accountant;
	}
	public void setRefund_accountant(String refund_accountant) {
		this.refund_accountant = refund_accountant;
	}
	public String getRefund_bank() {
		return refund_bank;
	}
	public void setRefund_bank(String refund_bank) {
		this.refund_bank = refund_bank;
	}
	@Override
	public String toString() {
		return "RefundRequestDTO [no=" + no + ", dealing_point=" + dealing_point + ", done=" + done + ", id=" + id
				+ ", request_date=" + request_date + ", refund_accountant=" + refund_accountant + ", refund_bank="
				+ refund_bank + "]";
	}
	
	
	
	
}
