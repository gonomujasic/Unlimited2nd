package ryu.qna.model;

import java.util.Arrays;

public class QnaDTO {
	private Integer qna_number;
	private String id;
	private String qna_title;
	private String qna_contents;
	private String write_date;
	private int viewcnt;
	private String[] files;
	
	public Integer getQna_number() {
		return qna_number;
	}

	public void setQna_number(Integer qna_number) {
		this.qna_number = qna_number;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getQna_title() {
		return qna_title;
	}

	public void setQna_title(String qna_title) {
		this.qna_title = qna_title;
	}

	public String getQna_contents() {
		return qna_contents;
	}

	public void setQna_contents(String qna_contents) {
		this.qna_contents = qna_contents;
	}

	public String getWrite_date() {
		return write_date;
	}

	public void setWrite_date(String write_date) {
		this.write_date = write_date;
	}

	public int getViewcnt() {
		return viewcnt;
	}

	public void setViewcnt(int viewcnt) {
		this.viewcnt = viewcnt;
	}

	public String[] getFiles() {
		return files;
	}

	public void setFiles(String[] files) {
		this.files = files;
	}

	@Override
	public String toString() {
		return "QnaDTO [qna_number=" + qna_number + ", id=" + id + ", qna_title=" + qna_title + ", qna_contents="
				+ qna_contents + ", write_date=" + write_date + ", viewcnt=" + viewcnt + ", files="
				+ Arrays.toString(files) + "]";
	}
}
