
package kjy.rating_history.dto;

public class RatingHistoryDTO {
private int review_number; // 후기번호
private int talking_speed; // 말속도
private int friendliness;  // 친절 
private int pronunciation; // 발음
private int worthy_of_content; // 수업내용
private int pleasure; // 재미
private String mentor_id; //멘토아이디
private String id; // 멘티아이디

public int getReview_numver() {
	return review_number;
}
public void setReview_numver(int review_numver) {
	this.review_number = review_numver;
}
public int getTalking_speed() {
	return talking_speed;
}
public void setTalking_speed(int talking_speed) {
	this.talking_speed = talking_speed;
}
public int getFriendliness() {
	return friendliness;
}
public void setFriendliness(int friendliness) {
	this.friendliness = friendliness;
}
public int getPronunciation() {
	return pronunciation;
}
public void setPronunciation(int pronunciation) {
	this.pronunciation = pronunciation;
}
public int getWorthy_of_content() {
	return worthy_of_content;
}
public void setWorthy_of_content(int worthy_of_content) {
	this.worthy_of_content = worthy_of_content;
}
public int getPleasure() {
	return pleasure;
}
public void setPleasure(int pleasure) {
	this.pleasure = pleasure;
}
public String getMentor_id() {
	return mentor_id;
}
public void setMentor_id(String mentor_id) {
	this.mentor_id = mentor_id;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
@Override
public String toString() {
	return "RatingHistoryDTO [review_numver=" + review_number + ", talking_speed=" + talking_speed + ", friendliness="
			+ friendliness + ", pronunciation=" + pronunciation + ", worthy_of_content=" + worthy_of_content
			+ ", pleasure=" + pleasure + ", mentor_id=" + mentor_id + ", id=" + id + "]";
}

}
