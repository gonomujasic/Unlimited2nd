package kjy.rating_history.dto;

import org.springframework.stereotype.Repository;

@Repository
public class AvgDTO {
	private double avgts;
	private double avgfr;
	private double avgpr;
	private double avgwoc;
	private double avgpl;
	private double totalavg;
	private String mentor_id;
	private int rank;
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getMentor_id() {
		return mentor_id;
	}
	@Override
	public String toString() {
		return "AvgDTO [avgts=" + avgts + ", avgfr=" + avgfr + ", avgpr=" + avgpr + ", avgwoc=" + avgwoc + ", avgpl="
				+ avgpl + ", totalavg=" + totalavg + ", mentor_id=" + mentor_id + ", rank=" + rank + "]";
	}
	public void setMentor_id(String mentor_id) {
		this.mentor_id = mentor_id;
	}
	public double getAvgts() {
		return avgts;
	}
	public void setAvgts(double avgts) {
		this.avgts = avgts;
	}
	public double getAvgfr() {
		return avgfr;
	}
	public void setAvgfr(double avgfr) {
		this.avgfr = avgfr;
	}
	public double getAvgpr() {
		return avgpr;
	}
	public void setAvgpr(double avgpr) {
		this.avgpr = avgpr;
	}
	public double getAvgwoc() {
		return avgwoc;
	}
	public void setAvgwoc(double avgwoc) {
		this.avgwoc = avgwoc;
	}
	public double getAvgpl() {
		return avgpl;
	}
	public void setAvgpl(double avgpl) {
		this.avgpl = avgpl;
	}
	public double getTotalavg() {
		return totalavg;
	}
	public void setTotalavg(double totalavg) {
		this.totalavg = totalavg;
	}
	
}
